public class MatematicaUtil {
    public static void quadrado(double numero) {
        System.out.println("O quadrado de " + numero + " é: " + (numero * numero));
    }
    
    public static void cubo(double numero) {
        System.out.println("O cubo de " + numero + " é: " + (numero * numero * numero));
    }

    public static void raiz(double numero) {
        System.out.println("A raíz quadrada de " + numero + " é: " + (Math.sqrt(numero)));
    }

}
